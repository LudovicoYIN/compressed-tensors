import torch
import torch.nn.functional as F


class DummyModel(torch.nn.Module):
    def __init__(self, input_dim=10, hidden_dim=16, output_dim=4):
        super().__init__()
        self.fc1 = torch.nn.Linear(input_dim, hidden_dim)
        self.fc2 = torch.nn.Linear(hidden_dim, output_dim)

    def forward(self, x):
        x = self.fc1(x)  # regular pass
        x = self.fc2.weight @ x.T  # direct parameter access
        return F.relu(x)
